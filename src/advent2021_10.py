from functools import reduce

points = {')': 3, ']': 57, '}': 1197, '>': 25137}
points2 = {'(': 1, '[': 2, '{': 3, '<': 4}
pairs = {'}': '{', '>': '<', ']': '[', ')': '('}

def parse_input(inp):
    return inp.split()

def corrupted_line(line, mem):
    if not line:
        return mem
    c = line[0]
    if c in ['(','<','[','{']:
        return corrupted_line(line[1:], mem + c)
    p = pairs[c]
    if mem[-1] != p:
        return c
    return corrupted_line(line[1:], mem[:-1])

def solve1(inp):
    corrupted = [corrupted_line(x, '') for x in inp]
    corrupted = [points[x] for x in corrupted if x in points.keys()]
    return sum(corrupted)

def score(elems):
    return reduce(lambda acc, val: acc*5 + points2[val], elems, 0)

def solve2(inp):
    incomplete = [corrupted_line(x, '') for x in inp]
    incomplete = [''.join(reversed(x)) for x in incomplete if x not in points.keys()]
    result = [score(x) for x in incomplete]
    result = sorted(result)
    return result[len(result) // 2]

inp = parse_input(inp)
r1 = solve1(inp)
print(r1)
r2 = solve2(inp)
print(r2)
